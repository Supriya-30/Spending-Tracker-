    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
           java.util.Date dt=d.getDate();
           String s1=a.getText();
           String s2 = a.getText();
           String c=(String)category.getSelectedItem();
           if(dt!=null && !s1.equals("") &&!s2.equals("") && !c.equals(""))
           {
               int id=Integer.parseInt(s2);
               int amount=Integer.parseInt(s1);
               java.sql.Date date=new java.sql.Date(dt.getTime());
             db.dbConnect.st.executeUpdate("insert into spendings(sid,s_category,s_date,amount) values("+id+",'"+c+"','"+date+"',"+amount+") ");
             JOptionPane.showMessageDialog(null,"Expenses added Successfully");
             getEntries();
           }else{
              JOptionPane.showMessageDialog(null, "Please fill all details"); 
           }
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

