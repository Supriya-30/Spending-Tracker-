      private void displayCategory()
       {
           try{
          category.removeAllItems();
          ResultSet rs= db.dbConnect.st.executeQuery("select * from category_info");
          while(rs.next())
          {
              category.addItem(rs.getString("category"));
          }
           }catch(Exception ex){
               JOptionPane.showMessageDialog(null, ex);
           }
       }
      private void getEntries()
      {
          try{
              javax.swing.table.DefaultTableModel dtm;
              dtm = (javax.swing.table.DefaultTableModel)Table1.getModel();
              int rc=dtm.getRowCount();
              while(rc--!=0)
              {
                  dtm.removeRow(0);
              }
             java.time.LocalDate cd=java.time.LocalDate.now();
              java.time.LocalDate bd=cd.minusDays(20);
             ResultSet rs=db.dbConnect.st.executeQuery("select * from spendings where s_date<='"+cd+"' and s_date>='"+bd+"'");
             int total=0;
             while(rs.next())
             {
                int t=rs.getInt("amount");
                total+=t;  
                System.out.println("ID:"+rs.getInt("sid")+", category:"+rs.getString("s_category")+", Date:"+rs.getDate("s_date")+",Amount:"+rs.getInt("amount"));
                Object o[]={rs.getInt("sid"),
                rs.getString("s_category"),
                rs.getDate("s_date"),
                rs.getInt("amount")};
                dtm.addRow(o);
             }
             totalAmount.setText(String.valueOf(total));
          }catch(Exception ex)
          {
             JOptionPane.showMessageDialog(null, ex);
          }
      }
